#define L2_HEADER_SIZE 14
static inline void dumpTxData(uint8_t *pHead, uint32_t len)
{
    uint32_t i, n;
    uint8_t *c = pHead;
    uint8_t buf[60];
    for (i = 0, n = 0; i < len; ++i) {
        if (i % 16 == 0) {
            if (i) printk("%s\n", buf);
            n = sprintf(buf, "%04x:", i);
        }
        if (i % 8 == 0)
            n += sprintf(&buf[n], "  %02x", *c++ );
        else
            n += sprintf(&buf[n], " %02x", *c++ );
    }
    printk("%s\n", buf);
}

